package potrace;


/**
 * Holds the coordinates of a Point.
 */
public class DoublePoint {

    //x-coordinate
    double X;


    //y-coordinate
    double Y;

    /**
     * Constructor
     *
     * @param x x-coordinate
     * @param y y-coordinate
     */
    public DoublePoint(double x, double y) {
        X = x;
        Y = y;
    }
}
